package ru.enikhov.lesson8.task3;

public class Act {
    private int numAct;
    private String dtAct;
    private String[] productAct;

    public void setNumAct(int numAct) {
        this.numAct = numAct;
    }

    public void setDtAct(String dtAct) {
        this.dtAct = dtAct;
    }

    public void setProductAct(String[] productAct) {
        this.productAct = productAct;
    }

    public int getNumAct() {
        return numAct;
    }

    public String getDtAct() {
        return dtAct;
    }

    public String[] getProductAct() {
        return productAct;
    }
}
